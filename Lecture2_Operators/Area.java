import java.util.*;


public class Area {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        double r = sc.nextInt();
        double pi = 3.14;
        
        double r_sq = r*r;

        double area_of_circle = pi * r_sq;

        System.out.println(area_of_circle);
        
        
        
    }
}

    

